add_action('vc_enqueue_font_icon_element', 'cynicSEO_load_vc_caviaricons');

function cynicSEO_load_vc_caviaricons($type)
{
    if ($type == 'caviaricons') {
        $path = apply_filters('cynicSEO_caviaricons_font_css_url', get_template_directory_uri() . '/css/seo-agency/cynic-caviar-icons.css');
        wp_enqueue_style('caviaricons', $path);
    }
}

function cynicSEO_load_backend_caviaricons($page)
{
    if (in_array($page, array('post.php', 'post-new.php'))) {
        $path = apply_filters('cynicSEO_caviaricons_font_css_url', get_template_directory_uri() . '/css/seo-agency/cynic-caviar-icons.css');
        wp_enqueue_style('caviaricons', $path);
    }
}

