add_action('vc_base_register_admin_css', 'cynic_vc_iconpicker_base_register_css');

function cynic_vc_iconpicker_base_register_css() {
    $path = apply_filters('cynic_linearicons_font_css_url', get_template_directory_uri() . '/css/classic-modern-agency/flaticon.css');
    wp_enqueue_style('flaticon', $path);
}


