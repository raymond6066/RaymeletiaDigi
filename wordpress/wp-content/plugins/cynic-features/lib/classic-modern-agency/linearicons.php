add_action('vc_enqueue_font_icon_element', 'cynic_load_vc_linearicons');

function cynic_load_vc_linearicons($type) {
    if ($type == 'linearicons') {
        $path = apply_filters('cynic_linearicons_font_css_url', get_template_directory_uri() . '/css/classic-modern-agency/linearicons-font.css');
        wp_enqueue_style('linearicons', $path);
    }
}

function cynic_load_backend_linearicons($page) {
    if (in_array($page, array('post.php', 'post-new.php'))) {
        $path = apply_filters('cynic_linearicons_font_css_url', get_template_directory_uri() . '/css/classic-modern-agency/linearicons-font.css');
        wp_enqueue_style('linearicons', $path);
    }
}

